def nested_cv_reg(estimator, space, n_iter, Xd, yd, g, outer):
    """회귀용 nested CV. 안쪽은 GroupKFold(역 단위) + neg-RMSE, 바깥은 분류와 동일 폴드."""
    oof = pd.Series(index=yd.index, dtype=float)
    for tr, te in outer:
        Xtr = Xd.iloc[tr].reset_index(drop=True)
        ytr = yd.iloc[tr].reset_index(drop=True)
        gtr = g.iloc[tr].reset_index(drop=True)
        inner = list(GroupKFold(INNER_SPLITS).split(Xtr, ytr, groups=gtr))
        s = RandomizedSearchCV(estimator, space, n_iter=n_iter,
                               scoring='neg_root_mean_squared_error',
                               cv=inner, random_state=RANDOM_STATE, n_jobs=-1, refit=True)
        s.fit(Xtr, ytr)
        oof.iloc[te] = s.best_estimator_.predict(Xd.iloc[te])
    return oof

